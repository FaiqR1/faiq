public class StrukturKontrol {
    
    public static void main(String[] args) {
        int umur = 19;
        // jika di masukkan umur 19 berarti yang akan tampil Anda belum tua
        // karena syarat untuk menampilkan Anda sudah Tua adalah 30
        if (umur == 30){
            System.out.println("Kamu Sudah Tua");
        }else{
            System.out.println("Kamu Belum Tua");
        }
        
    }
}
