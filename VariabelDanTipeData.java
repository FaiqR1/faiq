public class VariabelDanTipeData {

    public static void main(String[] args) {
        String nama, alamat;
        int usia;
        double tinggi;
        // pengisian variabel
        nama ="Faiq";
        alamat ="BPS G9/3";
        usia = 19;
        tinggi = 165;
        // tampilkan hasil

        System.out.println("Nama;"+nama);
        System.out.println("Alamat;"+alamat);
        System.out.println("Usia;"+usia);
        System.out.println("Tinggi;"+tinggi);
        
    }
    
}
